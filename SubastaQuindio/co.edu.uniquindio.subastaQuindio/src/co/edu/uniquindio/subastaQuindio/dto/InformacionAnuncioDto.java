package co.edu.uniquindio.subastaQuindio.dto;

public class InformacionAnuncioDto {
	protected String nombreProducto;
	protected String nombreAnunciante;
	protected String fechaLimite;
	protected String fechaPublicacion;
	protected String valorInicial;
	/**
	 * @return the nombreProducto
	 */
	public String getNombreProducto() {
		return nombreProducto;
	}
	/**
	 * @param nombreProducto the nombreProducto to set
	 */
	public void setNombreProducto(String nombreProducto) {
		this.nombreProducto = nombreProducto;
	}
	/**
	 * @return the nombreAnunciante
	 */
	public String getNombreAnunciante() {
		return nombreAnunciante;
	}
	/**
	 * @param nombreAnunciante the nombreAnunciante to set
	 */
	public void setNombreAnunciante(String nombreAnunciante) {
		this.nombreAnunciante = nombreAnunciante;
	}
	/**
	 * @return the fechaLimite
	 */
	public String getFechaLimite() {
		return fechaLimite;
	}
	/**
	 * @param fechaLimite the fechaLimite to set
	 */
	public void setFechaLimite(String fechaLimite) {
		this.fechaLimite = fechaLimite;
	}
	/**
	 * @return the fechaPublicacion
	 */
	public String getFechaPublicacion() {
		return fechaPublicacion;
	}
	/**
	 * @param fechaPublicacion the fechaPublicacion to set
	 */
	public void setFechaPublicacion(String fechaPublicacion) {
		this.fechaPublicacion = fechaPublicacion;
	}
	/**
	 * @return the valorInicial
	 */
	public String getValorInicial() {
		return valorInicial;
	}
	/**
	 * @param valorInicial the valorInicial to set
	 */
	public void setValorInicial(String valorInicial) {
		this.valorInicial = valorInicial;
	}
	
	
}
